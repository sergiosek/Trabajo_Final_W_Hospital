    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String num = txNum.getText();
        String nom = txtAp.getText();
        String prenom = txtNom.getText();
        String adresse = txtDir.getText();
        String tel = txtTel.getText();
        boolean valido = true;
        boolean error = false;
        boolean vacio = false;

        if("".equals(num) && "".equals(nom) && "".equals(prenom) && "".equals(adresse) && "".equals(tel)){
             JOptionPane.showMessageDialog(
                      this,
                      "Debe ingresar datos a todos los campos",
                      "Registro empleados",
                      JOptionPane.ERROR_MESSAGE);
             valido = false;
             vacio = true;
        }
        else{
             if(!error){
                 try{
                     int n = Integer.parseInt(num);
                      }catch(NumberFormatException nfe){
                         JOptionPane.showMessageDialog(
                               this,
                               "Solo se debe ingresar numeros en el campo numero",
                               "Registro empleados",
                               JOptionPane.ERROR_MESSAGE);
                         valido = false;
                         error = true;
                         txNum.setText("");
                      }
             }
             else if(!error){
                 try{
                int n = Integer.parseInt(tel);
                 }catch(NumberFormatException nfe){
                    JOptionPane.showMessageDialog(
                          this,
                          "Solo se debe ingresar numeros en el campo telefono",
                          "Registro empleados",
                          JOptionPane.ERROR_MESSAGE);
                    valido = false;
                    error = true;
                    txNum.setText("");
                 } 
             }  
             else if(!error){
                  try{
                     int n = Integer.parseInt(prenom);
                     JOptionPane.showMessageDialog(
                           this,
                           "Solo se debe ingresar letras en el campo nombre",
                           "Registro empleados",
                           JOptionPane.ERROR_MESSAGE);
                     valido = false;
                     error = true;
                     txNum.setText("");
                  }catch(NumberFormatException nfe){
                      if(!"".equals(prenom))
                         valido = true;               
                  }
             } 
             if(!error){
                  try{
                     int n = Integer.parseInt(nom);
                     JOptionPane.showMessageDialog(
                           this,
                           "Solo se debe ingresar letras en el campo apellido",
                           "Registro empleados",
                           JOptionPane.ERROR_MESSAGE);
                     valido = false;
                     error = true;
                     txNum.setText("");
                  }catch(NumberFormatException nfe){
                      if(!"".equals(nom))
                         valido = true;               
                  } 
             }
             else if(!error){
                 if("".equals(adresse)){
                  JOptionPane.showMessageDialog(
                      this,
                      "Debe ingresar valores en el campo dirección",
                      "Registro empleados",
                      JOptionPane.ERROR_MESSAGE);
                valido = false;
                error = true;
             }
             }
        }
        if(!vacio && rbMedico.isSelected()){
            if(cbEspecialidad.getSelectedItem().toString().equals("Seleccione una especialidad")){
            JOptionPane.showMessageDialog(
                      this,
                      "Seleccione una especialidad",
                      "Registro empleados",
                      JOptionPane.ERROR_MESSAGE);
                valido = false;
                error = true;                 
             }
             else{
                 if(!error)
                     valido = true;
             }
        }
        else if(!vacio && rbEnfermera.isSelected()){
            if(cbEnfSer.getSelectedItem().toString().equals("Seleccione un servicio")){
            JOptionPane.showMessageDialog(
                      this,
                      "Seleccione un servicio",
                      "Registro empleados",
                      JOptionPane.ERROR_MESSAGE);
                valido = false;
                error = true;                 
             }
             else{
                 if(!error)
                     valido = true;
             }
            try{
                 int n = Integer.parseInt(txtEnfSalario.getText());
             }catch(NumberFormatException nfe){
                 JOptionPane.showMessageDialog(
                      this,         
                      "Solo se debe ingresar numeros en el campo salario",         
                      "Registro empleados",         
                 JOptionPane.ERROR_MESSAGE);                      
                     valido = false;
                     error = true;
                     txNum.setText("");
             }             
             if(!error)
                valido = true;
        }

         if(valido){
             try {
             // TODO add your handling code here:
             new Conexion().ejecutarQuery("insert into employe(numero, nom, prenom, adresse, tel) "
                     + "values("+num+", '"+nom+"', '"+prenom+"', '"+adresse+"', '"+tel+"')");
         } catch (ClassNotFoundException | SQLException ex) {
             //ex.printStackTrace();
             JOptionPane.showMessageDialog(
                      this,         
                      "Numero dupliacado",         
                      "Registro empleados",         
                 JOptionPane.ERROR_MESSAGE);                      
                     valido = false;
                     error = true;
         }

         if(rbMedico.isSelected()){
             String esp = cbEspecialidad.getSelectedItem().toString();
             try {
             // TODO add your handling code here:
             new Conexion().ejecutarQuery("insert into docteur(numero, specialite) "
                     + "values("+num+", '"+esp+"')");
             } catch (ClassNotFoundException | SQLException ex) {
                 //ex.printStackTrace();
             }
         }
         else if(rbEnfermera.isSelected()){
             String serv = cbEnfSer.getSelectedItem().toString();
             String tur = cbEnfTur.getSelectedItem().toString();
             int sal = Integer.parseInt(txtEnfSalario.getText());
             try {
             // TODO add your handling code here:
             new Conexion().ejecutarQuery("insert into infirmier(numero, code_service,rotation,salaire) "
                     + "values("+num+", '"+serv+"', '"+tur+"', "+sal+")");
             } catch (ClassNotFoundException | SQLException ex) {
                 //ex.printStackTrace();
             }
         }
     }
        txNum.setText("");
        txtNom.setText("");
        txtAp.setText("");
        txtDir.setText("");
        txtTel.setText("");
        txtEnfSalario.setText("");
        cbEnfSer.setSelectedIndex(0);
        cbEspecialidad.setSelectedIndex(0);
        em = new EmpleadosTableModel();
        tEmpleados.setModel(em);
    }//GEN-LAST:event_btnRegistrarActionPerformed

