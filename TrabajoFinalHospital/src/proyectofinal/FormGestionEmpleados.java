    private void cargarComboBox() throws ClassNotFoundException, SQLException{
        //Servicios
        Conexion conn = new Conexion();
        servicios = conn.getServicios();
        
        for(Servicio serv: servicios){
            cbEnfSer.addItem(serv.getCodigo());
        }
        
        //Especialidades
        especialidades = conn.getEspecialidades();
        
        for(String esp: especialidades){
            cbEspecialidad.addItem(esp);
        }
    }

